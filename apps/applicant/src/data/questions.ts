// 5Cs of Credit Categories
export type CreditCategory = 'Character' | 'Capacity' | 'Capital' | 'Conditions' | 'Collateral';

// Construct types for CWI scoring
export type Construct =
  | 'payment_history'
  | 'financial_behaviour'
  | 'financial_management'
  | 'financial_resources'
  | 'emergency_preparedness'
  | 'crisis_decision_making'
  | 'crisis_management'
  | 'conscientiousness'
  | 'emotional_stability'
  | 'agreeableness'
  | 'openness'
  | 'extraversion'
  | 'risk_preference'
  | 'self_control'
  | 'locus_of_control'
  | 'social_support'
  | 'time_orientation'
  | 'cognitive_reflection'
  | 'delay_discounting'
  | 'financial_numeracy'
  | 'loan_consequence_awareness'
  | 'gaming_detection'
  | 'demographic';

export interface Question {
  id: string;
  number: number;
  text: string;
  type: 'scale' | 'select' | 'ranking' | 'text' | 'email';
  options: string[];
  category?: CreditCategory;
  categoryDescription?: string;
  isDemographic?: boolean;
  // Scoring fields
  construct?: Construct;
  reverseScored?: boolean; // For items like missed payments, stress, impulsivity
  fiveC?: CreditCategory; // Which of the 5Cs this maps to
  // ASFN fields
  correctAnswer?: string; // For ASFN objective scoring
  level?: 1 | 2; // ASFN level identifier
  // LCA fields
  lcaPoints?: Record<string, number>; // LCA point values for each option (A, B, C, D)
}

export interface Section {
  id: string;
  title: string;
  subtitle?: string;
  questions: Question[];
}

